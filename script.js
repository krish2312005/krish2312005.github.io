// Initialize typing effect when page loads
document.addEventListener('DOMContentLoaded', () => {
    const heroTitle = document.querySelector('.hero-title');
    if (!heroTitle) return;

    // The text before the highlight
    const before = "Hi, I'm ";
    // The highlighted name
    const name = "Krish Patel";
    // The text after the highlight (if any)
    const after = "";

    heroTitle.innerHTML = ""; // Clear

    let i = 0, j = 0, state = 'before';

    function type() {
        if (state === 'before' && i < before.length) {
            heroTitle.innerHTML += before.charAt(i++);
            setTimeout(type, 50);
        } else if (state === 'before') {
            // Start the highlight span
            heroTitle.innerHTML += '<span class="highlight"></span>';
            state = 'highlight';
            type();
        } else if (state === 'highlight' && j < name.length) {
            const span = heroTitle.querySelector('.highlight');
            span.textContent += name.charAt(j++);
            setTimeout(type, 50);
        } else if (state === 'highlight') {
            state = 'after';
            type();
        } else if (state === 'after' && after.length > 0) {
            heroTitle.innerHTML += after;
        }
    }

    type();
});

